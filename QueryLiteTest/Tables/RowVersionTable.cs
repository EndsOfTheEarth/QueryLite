using QueryLite;

namespace QueryLiteTest.Tables {

    public sealed class RowVersionTestTable : ATable {

        public static readonly RowVersionTestTable Instance = new RowVersionTestTable();

        public Column<int> Id { get; }
        public Column<byte[]> RowVersion { get; }

        public override PrimaryKey? PrimaryKey => new PrimaryKey(table: this, constraintName: "pk_RowVersionTest", Id);

        private RowVersionTestTable() : base(tableName: "RowVersionTest", schemaName: "dbo") {

            Id = new Column<int>(this, columnName: "Id");
            RowVersion = new Column<byte[]>(this, columnName: "RowVersion", isAutoGenerated: true);
        }
    }
}