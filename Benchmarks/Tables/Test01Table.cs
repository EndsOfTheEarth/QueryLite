        [
            new GetSetMap<Test01Row>(table.Id, row => row.Id, (row, resultRow) => row.Id = resultRow.Get(table.Id)),
            new GetSetMap<Test01Row>(table.Row_guid, row => row.Row_guid, (row, resultRow) => row.Row_guid = resultRow.Get(table.Row_guid)),
